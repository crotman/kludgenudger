    ) 
  }
  else{
    
    saida <- categorised_alerts %>% 
      mutate(
        file_new = code_file_new,
        file_old = code_file_old,
        time = Sys.time() - start  
      )      
    
  }
  

  write_rds(saida, str_glue("log/{uid}.rds"))

    
  saida
  
