          ) %>%
          replace_na(list(same_alert = FALSE)) %>%
          mutate(
            version = "old",
            category = if_else(.data$same_alert, "open", "fixed")
          )
        
        alerts_new <- graph_new_with_alert %>%
          activate("nodes") %>%
