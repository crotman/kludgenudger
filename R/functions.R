        .data$beginline,
        .data$endline,
        .data$rule,
        .data$id_group,
        .data$method,
        .data$rule_alert,
        .data$code
      ) %>% 
      rowwise() %>%  
      mutate( code = str_flatten(.data$code, collapse = "\n") ) %>% 
      ungroup() %>% 
      left_join(
        coordinates %>% select(-.data$new),
        by = c("beginline" = "old")
